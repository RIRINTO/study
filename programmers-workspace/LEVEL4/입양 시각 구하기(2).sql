SELECT B.HOUR AS HOUR,
        COUNT(ANIMAL_ID) AS COUNT
FROM ANIMAL_OUTS A
    RIGHT OUTER JOIN (
    SELECT 0 AS HOUR
    FROM DUAL
    UNION
    SELECT 1
    FROM DUAL
    UNION
    SELECT 2
    FROM DUAL
    UNION
    SELECT 3
    FROM DUAL
    UNION
    SELECT 4
    FROM DUAL
    UNION
    SELECT 5
    FROM DUAL
    UNION
    SELECT 6
    FROM DUAL
    UNION
    SELECT 7
    FROM DUAL
    UNION
    SELECT 8
    FROM DUAL
    UNION
    SELECT 9
    FROM DUAL
    UNION
    SELECT 10
    FROM DUAL
    UNION
    SELECT 11
    FROM DUAL
    UNION
    SELECT 12
    FROM DUAL
    UNION
    SELECT 13
    FROM DUAL
    UNION
    SELECT 14
    FROM DUAL
    UNION
    SELECT 15
    FROM DUAL
    UNION
    SELECT 16
    FROM DUAL
    UNION
    SELECT 17
    FROM DUAL
    UNION
    SELECT 18
    FROM DUAL
    UNION
    SELECT 19
    FROM DUAL
    UNION
    SELECT 20
    FROM DUAL
    UNION
    SELECT 21
    FROM DUAL
    UNION
    SELECT 22
    FROM DUAL
    UNION
    SELECT 23
    FROM DUAL
    ) B
ON(TO_NUMBER(TO_CHAR(A.DATETIME, 'HH24')) = B.HOUR)
GROUP BY B.HOUR
ORDER BY B.HOUR;